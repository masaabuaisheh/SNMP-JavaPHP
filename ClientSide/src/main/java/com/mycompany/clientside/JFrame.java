     void sendData_POST() {
    String name = nameField.getText();  
    String password = passwordField.getText();  
    
    addParameter_jsp("nameField",name);
    addParameter_jsp("passwordField",password);

    String urlStr = this.jTextField4.getText(); 

    // Prepare the data to be sent
        try {
            URL myURL = new URL(urlStr);
            HttpURLConnection myConn = (HttpURLConnection)myURL.openConnection();
            myConn.setDoOutput(true);
            myConn.setDoInput(true);
            myConn.setRequestProperty("Content-Type", contentStr);
            myConn.setUseCaches(false);
            myConn.setRequestMethod("POST");
            this.jTextField3.setText("POST: sending to " + urlStr+" data:"+dataStrjsp);
            BufferedOutputStream out = new BufferedOutputStream(myConn.getOutputStream());
            out.write(dataStrjsp.getBytes());
            out.close();
            
        // Read response
        String response = "";
         int b = -1;
       InputStream is = myConn.getInputStream();
            
            while ((b = is.read()) != -1) {
                response = response + ((char) b);
            }
        
        // Display response in text area
        String responseStr = response.toString().trim(); // Trim the response
        this.jTextArea1.setText(responseStr);
       // System.out.println("Response from server: '" + responseStr + "'"); // Debugging

        // Additional checks
        String cleanedResponse = responseStr.replaceAll("\\s+", "");
      //  System.out.println("Response Length: " + responseStr.length());

        if ("Permit".equalsIgnoreCase(cleanedResponse)) {
            this.jTextArea1.append("\n Access Granted.");
             jspAuthenticated = true;
             if(jspAuthenticated){
             JOptionPane.showMessageDialog(this, "Access Granted.", "Success", JOptionPane.INFORMATION_MESSAGE);
             jTextArea1.setText(null);
             jTextField3.setText(null);
             passwordField.setText(null); 
             nameField.setText(null);
            }
             System.out.println( jspAuthenticated);
            
             

        } else {
            this.jTextArea1.append("\n Access Denied.");
             jspAuthenticated = false;
            if(!jspAuthenticated){
            JOptionPane.showMessageDialog(this, "Access Denied.", "Not Success", JOptionPane.INFORMATION_MESSAGE);
             jTextArea1.setText(null);
             jTextField3.setText(null);
             passwordField.setText(null);    
             nameField.setText(null);
            }
             System.out.println( jspAuthenticated);
             
        }
        
        
      //  checkAuthenticationAndFetchData();

    } catch (Exception e) {
        System.out.println(e.toString());
        this.jTextArea1.setText("Failed: exception");
    }
    
            
     }
