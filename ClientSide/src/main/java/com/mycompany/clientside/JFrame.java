   public void addParameter_servlet(String key, String value) {
        if (key == null || value == null || key.length() == 0 || value.length() == 0) {
            return;
        }
    try {
        if (dataStrservlet.length() > 0) {
            dataStrservlet += "&";
        }
        dataStrservlet += URLEncoder.encode(key, "UTF-8") + "=" + URLEncoder.encode(value, "UTF-8");
    } catch (UnsupportedEncodingException e) {
        e.printStackTrace();
    }
   }
   
