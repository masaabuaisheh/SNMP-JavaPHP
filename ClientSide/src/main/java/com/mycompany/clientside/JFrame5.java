    private void updateSNMPValues() {
        String sysContactValue = jTextField1.getText();
        String sysNameValue = jTextField2.getText();
        String sysLocationValue = jTextField3.getText();

        // Use cURL or another HTTP client library to update the SNMP values
        ProcessBuilder pb = new ProcessBuilder("curl", "-s", "http://localhost/snmp/update.php", 
            "--data", "sysContact=" + sysContactValue, 
            "--data", "sysName=" + sysNameValue, 
            "--data", "sysLocation=" + sysLocationValue);

        pb.redirectErrorStream(true);

        try {
            Process process = pb.start();
            process.waitFor();
        } catch (IOException | InterruptedException e) {
            e.printStackTrace();
        }
    }
