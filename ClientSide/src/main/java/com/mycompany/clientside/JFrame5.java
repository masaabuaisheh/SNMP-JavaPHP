    private void fetchAndPopulateTextArea() {
        StringBuilder data = new StringBuilder();
        Process process;

        try {
            // Use cURL to fetch the PHP script's output
            ProcessBuilder pb = new ProcessBuilder("curl", "-s", "http://localhost/snmp/systemgroup.php");
            pb.redirectErrorStream(true);
            process = pb.start();

            // Read the output of the PHP script
            BufferedReader reader = new BufferedReader(new InputStreamReader(process.getInputStream()));
            String line;

            while ((line = reader.readLine()) != null) {
                // Append each line of SNMP data to the StringBuilder
                data.append(line).append("\n");
            }

            // Wait for the cURL process to complete
            process.waitFor();
            reader.close();
        } catch (IOException | InterruptedException e) {
            e.printStackTrace();
        }

        // Set the SNMP data in the JTextArea
        jTextArea1.setText(data.toString());
    }

    
